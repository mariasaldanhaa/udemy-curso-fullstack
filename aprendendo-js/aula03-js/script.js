// while - enquanto
var x = 0;

while (x < 10) {
    document.writeln("<br> O valor de X é: " + x); // a tag br faz a quebra de linha
    // aumentando o valor do x
    x++;
}
// for - para
var valor = 15;

for (a = 0; a < valor; a++ ) {
    document.writeln("<br> O valor do a é: " + a);
    console.log(a + 10);
}